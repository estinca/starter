package com.est.app.exception;

public class JsonConfigException extends Exception {

	private static final long serialVersionUID = 1L;

	public JsonConfigException() {
	}

	public JsonConfigException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public JsonConfigException(String arg0) {
		super(arg0);
	}
}
	
